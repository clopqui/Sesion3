public class Calculadora {

    public static int resta(int i, int j) {
        return i-j;
    }

    public static int multiplica(int i, int j) {
        return i*j;
    }

    public static int suma(int i, int j) {
        return i+j;
    }

    public static int divide(int i, int j) {
        return i/j;
    }
    
}
